# 1. Find all unique categories from the menu.
categories = set(item["category"] for item in menu.values())

# 2. Print category grouped menu items.
for category in categories:
    print(f"Category: {category}")
    for item, details in menu.items():      # loop through menu items to find those that belong to the current category
        if details["category"] == category:
            status = "Available" if details["available"] else "Not Available" # check availability status
            print(f"  - {item}: ${details['price']} [{status}]")
    print()
    
# 3. Total number of items in the menu.
total_items = len(menu)

# 4. Number of available items in the menu.
available_items = sum(1 for item in menu.values() if item["available"])

# 5. Find the most expensive item in the menu.
most_expensive_item = max(menu.items(), key=lambda x: x[1]["price"])

# 6. Find items under $150.
items_under_150 = [(name, data["price"]) for name, data in menu.items() if data["price"] < 150]

# Menu Summary
print("MENU SUMMARY")
